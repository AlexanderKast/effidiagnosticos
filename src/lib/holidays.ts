function toLocalDateStr(date: Date): string {
  const y = date.getFullYear();
  const m = String(date.getMonth() + 1).padStart(2, '0');
  const d = String(date.getDate()).padStart(2, '0');
  return `${y}-${m}-${d}`;
}

function resolveCountry(country: string): string {
  const c = (country ?? '').trim().toUpperCase();
  return c || 'CO';
}

/** Devuelve true si la fecha es festivo en el país dado */
export function isHoliday(date: Date, country: string): boolean {
  const dateStr = toLocalDateStr(date);
  const countryHolidays = HOLIDAYS[resolveCountry(country)] ?? [];
  return countryHolidays.includes(dateStr);
}

/** Devuelve true si la fecha es fin de semana o festivo */
export function isWeekendOrHoliday(date: Date, country: string): boolean {
  const dow = date.getDay();
  if (dow === 0 || dow === 6) return true;
  return isHoliday(date, country);
}

/** Conjunto de fechas festivas como strings YYYY-MM-DD para un país */
export function getHolidaySet(country: string): Set<string> {
  return new Set(HOLIDAYS[resolveCountry(country)] ?? []);
}
